package com.kn.array;

public class SwappingOfThreeNumber {

	public static void main(String[] args) {
		int a=10;
		int b=20;
		int c=30;
		//swap values
		int temp;
		temp=a;
		a=b;
		b=c;
		c=temp;
		System.out.println("a="+a);
		System.out.println("b="+b);
		System.out.println("c="+c);
		
	}

}
